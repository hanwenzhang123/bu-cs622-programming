package map;

public class Position {
    private int xAxis;
    private int yAxis;

    //Getter
    public int getxAxis() {
        return xAxis;
    }
    public int getyAxis() {
        return yAxis;
    }

    //Setter
    public void setxAxis(int xAxis) {
        this.xAxis = xAxis;
    }
    public void setyAxis(int yAxis) {
        this.yAxis = yAxis;
    }
}
